/**
 * Handoff Document Generator
 *
 * Generates structured markdown handoff documents between workflow phases.
 * Ensures consistent information transfer and maintains context across phase transitions.
 */

const path = require('path');
const fs = require('fs');
const fsPromises = require('fs').promises;
const { resolveEccRoot } = require('./resolve-ecc-root');

// Default ECC root resolver
function getEccRoot(baseDir) {
  if (baseDir) return baseDir;
  try {
    return resolveEccRoot().eccRoot;
  } catch (_e) {
    // Fallback to old behavior if resolveEccRoot fails
    return path.join(__dirname, '../../..');
  }
}

/**
 * Generate a handoff document for a phase transition
 * @param {Object} options - Handoff options
 * @param {string} options.requirementId - The requirement ID
 * @param {string} options.requirementName - The requirement name
 * @param {string} options.fromPhase - The source phase name
 * @param {string} options.toPhase - The target phase name
 * @param {Object} options.phaseResult - Result data from the completed phase
 * @param {string} options.phaseResult.summary - What was accomplished
 * @param {Array} options.phaseResult.decisions - Key decisions made
 * @param {Array} options.phaseResult.artifacts - Artifacts created/modified
 * @param {Array} options.phaseResult.openQuestions - Questions for next phase
 * @param {Array} options.phaseResult.nextSteps - Recommended next steps
 * @param {Object} options.phaseResult.metrics - Optional metrics (lines added/removed, etc.)
 * @param {string} options.nextAgent - Optional recommended next Agent
 * @param {string} options.nextAgentReason - Optional reason for Agent recommendation
 * @returns {string} - Generated markdown handoff document
 */
function generateHandoff({ requirementId, requirementName, fromPhase, toPhase, phaseResult, nextAgent, nextAgentReason }) {
  const timestamp = new Date().toISOString();
  const metrics = phaseResult.metrics || {};

  let artifactsTable = '';
  if (phaseResult.artifacts && phaseResult.artifacts.length > 0) {
    artifactsTable = phaseResult.artifacts.map(artifact => {
      const name = artifact.name || artifact.path || 'Unnamed';
      const artifactPath = artifact.path || '-';
      const status = artifact.status || 'created';
      return `| ${name} | ${artifactPath} | ${status} |`;
    }).join('\n');
  } else {
    artifactsTable = '| - | - | - |';
  }

  let decisionsList = '';
  if (phaseResult.decisions && phaseResult.decisions.length > 0) {
    decisionsList = phaseResult.decisions.map(d => `- ${d}`).join('\n');
  } else {
    decisionsList = '- (No major decisions)';
  }

  let openQuestionsList = '';
  if (phaseResult.openQuestions && phaseResult.openQuestions.length > 0) {
    openQuestionsList = phaseResult.openQuestions.map(q => `- ${q}`).join('\n');
  } else {
    openQuestionsList = '- (None)';
  }

  let nextStepsList = '';
  if (phaseResult.nextSteps && phaseResult.nextSteps.length > 0) {
    nextStepsList = phaseResult.nextSteps.map((step, i) => `${i + 1}. ${step}`).join('\n');
  } else {
    nextStepsList = '- (Continue with standard workflow)';
  }

  let agentRecommendation = '';
  if (nextAgent) {
    agentRecommendation = `**Next Agent**: ${nextAgent}`;
    if (nextAgentReason) {
      agentRecommendation += `\n**Reason**: ${nextAgentReason}`;
    }
  } else {
    agentRecommendation = '**Next Agent**: (No specific Agent recommended - will use phase default)';
  }

  return `# HANDOFF: ${fromPhase} → ${toPhase}

**Requirement**: ${requirementName} (${requirementId})
**Generated**: ${timestamp}

---

## Phase Summary

${phaseResult.summary || '(No summary provided)'}

---

## Key Decisions

${decisionsList}

---

## Artifacts

| Artifact | Path | Status |
|----------|------|--------|
${artifactsTable}

---

## Metrics

| Metric | Value |
|--------|-------|
| Lines Added | ${metrics.linesAdded || 0} |
| Lines Removed | ${metrics.linesRemoved || 0} |
| Tests Added | ${metrics.testsAdded || 0} |
| Coverage | ${metrics.coverage || 'N/A'} |

---

## Open Questions

${openQuestionsList}

---

## Next Steps

${nextStepsList}

---

## Agent Recommendation

${agentRecommendation}

---

*This handoff was automatically generated by the ECC Workflow Integration*
`;
}

/**
 * Save a handoff document to disk
 * @param {string} handoffContent - The markdown content
 * @param {string} requirementId - The requirement ID
 * @param {string} fromPhase - The source phase
 * @param {string} toPhase - The target phase
 * @param {string} baseDir - Base directory for handoffs (optional)
 * @returns {Promise<string>} - The file path where the handoff was saved
 */
async function saveHandoff(handoffContent, requirementId, fromPhase, toPhase, baseDir = null) {
  const eccRoot = getEccRoot(baseDir);
  const handoffsDir = path.join(eccRoot, '.requirements', 'handoffs', requirementId);

  await fsPromises.mkdir(handoffsDir, { recursive: true });

  const filename = `${fromPhase}_to_${toPhase}.md`;
  const filepath = path.join(handoffsDir, filename);

  await fsPromises.writeFile(filepath, handoffContent, 'utf-8');

  return filepath;
}

/**
 * Load a handoff document from disk
 * @param {string} requirementId - The requirement ID
 * @param {string} fromPhase - The source phase
 * @param {string} toPhase - The target phase
 * @param {string} baseDir - Base directory (optional)
 * @returns {Promise<string|null>} - The handoff content or null if not found
 */
async function loadHandoff(requirementId, fromPhase, toPhase, baseDir = null) {
  const eccRoot = getEccRoot(baseDir);
  const filepath = path.join(eccRoot, '.requirements', 'handoffs', requirementId, `${fromPhase}_to_${toPhase}.md`);

  try {
    return await fsPromises.readFile(filepath, 'utf-8');
  } catch (err) {
    // File not found is acceptable, other errors should be logged
    if (err.code !== 'ENOENT') {
      console.error(`Error loading handoff from ${filepath}:`, err.message);
    }
    return null;
  }
}

/**
 * List all handoffs for a requirement
 * @param {string} requirementId - The requirement ID
 * @param {string} baseDir - Base directory (optional)
 * @returns {Promise<Array>} - Array of handoff file info
 */
async function listHandoffs(requirementId, baseDir = null) {
  const eccRoot = getEccRoot(baseDir);
  const handoffsDir = path.join(eccRoot, '.requirements', 'handoffs', requirementId);

  try {
    const files = await fsPromises.readdir(handoffsDir);
    const results = [];

    for (const f of files) {
      if (!f.endsWith('.md')) continue;
      const filepath = path.join(handoffsDir, f);
      try {
        const stats = await fsPromises.stat(filepath);
        results.push({
          filename: f,
          filepath,
          createdAt: stats.birthtime,
          modifiedAt: stats.mtime
        });
      } catch (err) {
        console.error(`Error stating handoff file ${filepath}:`, err.message);
      }
    }

    return results.sort((a, b) => a.createdAt - b.createdAt);
  } catch {
    return [];
  }
}

module.exports = {
  generateHandoff,
  saveHandoff,
  loadHandoff,
  listHandoffs
};
